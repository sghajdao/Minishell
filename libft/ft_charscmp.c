#include "libft.h"

int	ft_charscmp(char *s1, char *s2)
{
	int	i;
	int	j;

	i = 0;
	while (s1[i])
	{
		j = 0;
		while (s2[j])
		{
			if (s1[i] == s2[j])
				return (0);
			j++;
		}
		i++;
	}
	return (1);
}
